import java.util.*

fun main(args: Array<String>) {
  val sc = Scanner(System.`in`)

  val curupira = sc.nextInt()
  val boitata = sc.nextInt()
  val boto = sc.nextInt()
  val mapinguari = sc.nextInt()
  val iara = sc.nextInt()

  println(225 + 300 * curupira + 1500 * boitata + 600 * boto + 1000 * mapinguari + 150 * iara)
}
